/* Tenary can make code look better, for example in if statements */

let x = 5


if (x == 5){;
console.log("match")
}

else if (x != 5){
console.log("unmatched")

}

/* We can do the same using tenary instead, ? = if, : = else, ( condition ? statement : statement ;) */

console.log(x == 5 ? 'match' : 'unmatched');